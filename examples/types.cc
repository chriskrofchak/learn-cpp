#include <iostream>

int main() {
    int  x = 3; // this is an integer
    char c = 'v'; // this is a character
    float fl = 3.5; // "float" is "floating point" which is a decimal
    bool am_i_crazy = true;
    bool am_i_hot = false;

    std::cout << "x is: " << x << std::endl;
    std::cout << "c is: " << c << std::endl;
    std::cout << "fl is: " << fl << std::endl;
    std::cout << "am_i_crazy is: " << am_i_crazy << std::endl;
    std::cout << "am_i_hot is: " << am_i_hot << std::endl;
}